// Tipos de dados
/*
--> No JavaScript temos diversos tipos de dados, os mais 
comuns e utilizados são: Number, String, Boolean, null e 
undefined, Object.

--> Podemos verificar o tipo do dado com o operador typeof.
*/

// Number
/*
--> O Number é o tipo de dado para números.

--> Possui três valores simbólicos: +Infinity, -Infinity e NaN.

--> Não existe um tipo definido para inteiros, todo número é um
Number.
 */

var numero = 76;

console.log(numero);
console.log(typeof numero);

var float = 5.32;

console.log(float);
console.log(typeof float);

// Número do tipo = String
var textoComNumero = '837';

console.log(textoComNumero);
console.log(typeof textoComNumero);

console.log(NaN)
console.log(typeof NaN)
console.log(typeof +Infinity)
console.log(typeof -Infinity)